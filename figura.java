/**
Representa estructura basica de figura geometricas.
LAs subclases deben implementar los metodos para calcular area y perimetro.

@author Pablo
*/
public abstract class figura{
	/**
	Retorna el area de la figura
	@return Area como double
	*/
	public abstract double area();
	/**
	Retorna el 
	@return Perimetro como double.
	*/
	public abstract double perimetro();
}
